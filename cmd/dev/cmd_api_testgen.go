package dev

import (
	"text/template"

	core "dappco.re/go"
	"dappco.re/go/cli/pkg/cli"
	"dappco.re/go/i18n"
	coreio "dappco.re/go/io"
)

func addTestGenCommand(parent *cli.Command) {
	testGenCmd := &cli.Command{
		Use:   "test-gen",
		Short: i18n.T("cmd.dev.api.test_gen.short"),
		Long:  i18n.T("cmd.dev.api.test_gen.long"),
		RunE: func(cmd *cli.Command, args []string) error {
			if r := runTestGen(); !r.OK {
				return cli.Wrap(r.Value.(error), i18n.Label("error"))
			}
			cli.Text(i18n.T("i18n.done.sync", "public API tests"))
			return nil
		},
	}

	parent.AddCommand(testGenCmd)
}

func runTestGen() (_ core.Result) {
	pkgDir := "pkg"
	internalDirs, err := coreio.Local.List(pkgDir)
	if err != nil {
		return core.Fail(cli.Wrap(err, "failed to read pkg directory"))
	}

	for _, dir := range internalDirs {
		if !dir.IsDir() || dir.Name() == "core" {
			continue
		}

		serviceName := dir.Name()
		internalDir := core.PathJoin(pkgDir, serviceName)
		publicDir := serviceName
		publicTestFile := core.PathJoin(publicDir, serviceName+"_test.go")

		if !coreio.Local.Exists(internalDir) {
			continue
		}

		symbols, r := getExportedSymbols(internalDir)
		if !r.OK {
			return core.Fail(cli.Wrap(r.Value.(error), cli.Sprintf("error getting symbols for service '%s'", serviceName)))
		}

		if len(symbols) == 0 {
			continue
		}

		if r := generatePublicAPITestFile(publicDir, publicTestFile, serviceName, symbols); !r.OK {
			return core.Fail(cli.Wrap(r.Value.(error), cli.Sprintf("error generating public API test file for service '%s'", serviceName)))
		}
	}

	return core.Ok(nil)
}

const publicAPITestTemplate = `// Code generated by "core dev api test-gen"; DO NOT EDIT.
package {{.ServiceName}}

import (
	impl "dappco.re/go/cli/{{.ServiceName}}"
)

{{range .Symbols}}
{{- if eq .Kind "type"}}
type _ = impl.{{.Name}}
{{- else if eq .Kind "const"}}
const _ = impl.{{.Name}}
{{- else if eq .Kind "var"}}
var _ = impl.{{.Name}}
{{- else if eq .Kind "func"}}
var _ = impl.{{.Name}}
{{- end}}
{{end}}
`

func generatePublicAPITestFile(dir, path, serviceName string, symbols []symbolInfo) (_ core.Result) {
	if err := coreio.Local.EnsureDir(dir); err != nil {
		return core.Fail(err)
	}

	tmpl, err := template.New("publicAPITest").Parse(publicAPITestTemplate)
	if err != nil {
		return core.Fail(err)
	}

	data := struct {
		ServiceName string
		Symbols     []symbolInfo
	}{
		ServiceName: serviceName,
		Symbols:     symbols,
	}

	buf := core.NewBuffer()
	if err := tmpl.Execute(buf, data); err != nil {
		return core.Fail(err)
	}

	return core.ResultOf(nil, coreio.Local.Write(path, buf.String()))
}
